package com.sb.sbInfo.domain.ZMPrinter;

public class LabelFormat {
    private Integer labelwidth;
    private Integer labelheight;
    private Integer labelrowgap;


    public Integer getLabelwidth() {
        return this.labelwidth;
    }

    public void setLabelwidth(Integer labelwidth) {
        this.labelwidth = labelwidth;
    }

    public Integer getLabelheight() {
        return this.labelheight;
    }

    public void setLabelheight(Integer labelheight) {
        this.labelheight = labelheight;
    }

    public Integer getLabelrowgap() {
        return this.labelrowgap;
    }

    public void setLabelrowgap(Integer labelrowgap) {
        this.labelrowgap = labelrowgap;
    }
}
