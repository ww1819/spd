-- 设备信息管理表
CREATE TABLE `equipment_info` (
  `id` varchar(32) NOT NULL COMMENT '主键ID',
  `asset_code` varchar(50) DEFAULT NULL COMMENT '资产编号',
  `hospital_code` varchar(50) DEFAULT NULL COMMENT '院内编码',
  `barcode` varchar(50) DEFAULT NULL COMMENT '条码号',
  `asset_name` varchar(200) NOT NULL COMMENT '资产名称',
  `asset_alias` varchar(200) DEFAULT NULL COMMENT '资产别名',
  `auxiliary_category` varchar(50) DEFAULT NULL COMMENT '辅助分类',
  `asset_status` char(1) NOT NULL DEFAULT '1' COMMENT '资产状态（0停用 1启用）',
  `financial_category` varchar(50) DEFAULT NULL COMMENT '财务分类',
  `financial_code` varchar(50) DEFAULT NULL COMMENT '财务编号',
  `brand` varchar(100) DEFAULT NULL COMMENT '品牌',
  `origin` varchar(100) DEFAULT NULL COMMENT '产地',
  `country` varchar(50) DEFAULT NULL COMMENT '国别',
  `specification` varchar(200) DEFAULT NULL COMMENT '规格',
  `model` varchar(100) DEFAULT NULL COMMENT '型号',
  `unit` varchar(20) DEFAULT NULL COMMENT '单位',
  `archive_code` varchar(50) DEFAULT NULL COMMENT '档案编号',
  `serial_number` varchar(100) DEFAULT NULL COMMENT '资产序列号',
  `asset_manager` varchar(50) DEFAULT NULL COMMENT '资产负责人',
  `maintenance_manager` varchar(50) DEFAULT NULL COMMENT '维修负责人',
  `use_department` varchar(100) DEFAULT NULL COMMENT '使用科室',
  `manage_department` varchar(100) DEFAULT NULL COMMENT '管理科室',
  `storage_location` varchar(200) DEFAULT NULL COMMENT '存放地点',
  `asset_type` varchar(50) DEFAULT NULL COMMENT '资产类型',
  `factory_number` varchar(100) DEFAULT NULL COMMENT '出厂编号',
  `production_date` date DEFAULT NULL COMMENT '出厂日期',
  `registration_number` varchar(100) DEFAULT NULL COMMENT '注册证件号',
  `expected_operation_date` date DEFAULT NULL COMMENT '预计开机工作日',
  `fund_source` varchar(100) DEFAULT NULL COMMENT '资金来源',
  `invoice_number` varchar(100) DEFAULT NULL COMMENT '发票编号',
  `invoice_amount` decimal(15,2) DEFAULT '0.00' COMMENT '发票金额',
  `attached_asset_flag` char(1) DEFAULT '0' COMMENT '附资产标志（0否 1是）',
  `emergency_asset` char(1) DEFAULT '0' COMMENT '急救资产（0否 1是）',
  `special_asset` char(1) DEFAULT '0' COMMENT '特种资产（0否 1是）',
  `measurement_asset` char(1) DEFAULT '0' COMMENT '计量资产（0否 1是）',
  `attached_materials` text COMMENT '附属资料（JSON格式）',
  `benefit_analysis` char(1) DEFAULT '0' COMMENT '效益分析（0否 1是）',
  `remark` text COMMENT '备注',
  `power` varchar(50) DEFAULT NULL COMMENT '设备功率',
  `theory_operation_time` int DEFAULT '0' COMMENT '理论开机时间（小时/天）',
  `public_equipment` char(1) DEFAULT '0' COMMENT '公用设备（0否 1是）',
  `creator` varchar(50) DEFAULT NULL COMMENT '录入人',
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '录入日期',
  `modifier` varchar(50) DEFAULT NULL COMMENT '最后修改人',
  `modify_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '最后修改时间',
  `archive_usage` char(1) DEFAULT '0' COMMENT '档案使用情况（0否 1是）',
  `contract_consistent` char(1) DEFAULT '0' COMMENT '合同与清单是否一致（0否 1是）',
  `special_network_asset` char(1) DEFAULT '0' COMMENT '是否专网资产（0否 1是）',
  `building` varchar(100) DEFAULT NULL COMMENT '楼宇',
  `floor` varchar(50) DEFAULT NULL COMMENT '楼层',
  `quantity` int DEFAULT '1' COMMENT '数量',
  `contract_name` varchar(200) DEFAULT NULL COMMENT '合同名称',
  `contract_price` decimal(15,2) DEFAULT '0.00' COMMENT '合同价格',
  `sign_date` date DEFAULT NULL COMMENT '签订日期',
  `purchase_date` date DEFAULT NULL COMMENT '购入日期',
  `acceptance_date` date DEFAULT NULL COMMENT '验收合格日期',
  `review_time` datetime DEFAULT NULL COMMENT '送审时间',
  `warranty_expire_date` date DEFAULT NULL COMMENT '保修到期',
  `bid_date` date DEFAULT NULL COMMENT '中标日期',
  `supply_notice_date` date DEFAULT NULL COMMENT '通知供货时间',
  `first_acceptance_date` date DEFAULT NULL COMMENT '首次验收日期',
  `delivery_deadline` date DEFAULT NULL COMMENT '交货截止日期',
  `second_acceptance_date` date DEFAULT NULL COMMENT '二次验收日期',
  `warranty_out_date` date DEFAULT NULL COMMENT '出保日期',
  `supplier` varchar(200) DEFAULT NULL COMMENT '供应商',
  `supplier_contact` varchar(50) DEFAULT NULL COMMENT '供应商联系人',
  `supplier_phone` varchar(20) DEFAULT NULL COMMENT '供应电话',
  `maintenance_company` varchar(200) DEFAULT NULL COMMENT '维修公司',
  `maintenance_contact` varchar(50) DEFAULT NULL COMMENT '维修联系人',
  `maintenance_phone` varchar(20) DEFAULT NULL COMMENT '维修电话',
  `manufacturer` varchar(200) DEFAULT NULL COMMENT '生产厂商',
  `purchase_method` varchar(50) DEFAULT NULL COMMENT '采购方式',
  `bidding_form` varchar(50) DEFAULT NULL COMMENT '招标形式',
  `single_budget` decimal(15,2) DEFAULT '0.00' COMMENT '单项预算',
  `project_basis` text COMMENT '立项依据',
  `bidding_number` varchar(100) DEFAULT NULL COMMENT '招标编号',
  `bidding_date` date DEFAULT NULL COMMENT '招标日期',
  `bidding_amount` decimal(15,2) DEFAULT '0.00' COMMENT '中标金额',
  `del_flag` char(1) DEFAULT '0' COMMENT '删除标志（0代表存在 2代表删除）',
  `create_by` varchar(64) DEFAULT '' COMMENT '创建者',
  `update_by` varchar(64) DEFAULT '' COMMENT '更新者',
  `update_time` datetime DEFAULT NULL COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_asset_code` (`asset_code`),
  KEY `idx_asset_name` (`asset_name`),
  KEY `idx_use_department` (`use_department`),
  KEY `idx_asset_status` (`asset_status`),
  KEY `idx_create_time` (`create_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='设备信息管理表';

